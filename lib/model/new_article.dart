class NewsArticle {
  final String title;
  final String source;
  final String timeAgo;
  final String views;
  final String comments;
  final String imageUrl;

  NewsArticle({
    required this.title,
    required this.source,
    required this.timeAgo,
    required this.views,
    required this.comments,
    required this.imageUrl,
  });
}